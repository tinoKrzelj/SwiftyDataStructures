import Foundation

extension String {
    struct linkedList {
        static var errorNodeValue: String { "*" }
        static var linkedListEmpty: String { "Empty" }
    }
    
    struct queue {
        static var printPlaceholder: String { "%@1 impl. based on %@2" }
    }
    
    struct tree {
        static var empty: String { "Empty" }
    }
}
